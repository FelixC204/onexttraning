<?php 
// Switch case
$color = "red";
switch ($color) {
    case "red":
        echo "Màu yêu thích của bạn là đỏ!";
        break;
    case "blue":
        echo "Màu yêu thích của bạn là xanh!";
        break;
    case "green":
        echo "Màu yêu thích của bạn là xanh lá!";
        break;
    default:
        echo "Màu yêu thích của bạn không nằm trong danh sách!";
}
?>